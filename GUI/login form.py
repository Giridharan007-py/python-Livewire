e1=Entry(r)
e1.place(x=170,y=80,width=175)
e2=Entry(r)
e2.place(x=170,y=120,width=175)
e3=Entry(width=40,show="*")
e3.place(x=170,y=160,width=175,)
e4=Entry(r)
e4.place(x=170,y=200,width=175)
def submit():
    name=e1.get()
    email=e2.get()
    password=e3.get()
    phone=e4.get()
    if not name or not email or not password or not phone:
        print("incomplete","Please fill all the fields")
        return
    b=Button(r,text="Submit",command=submit)
    b.place(x=150,y=250)
    f=open("app.txt", "a")
    f.write("\n")
    f.write(f" Name: {name}\n")
    f.write(f"email:{email}\n")
    f.write(f"password: {password}\n")
    f.write(f"Phone Number: {phone}\n")
    print("Account created!\n")
b=Button(r,text="Submit",bg="#3399ff",command=submit)
b.place(x=150,y=250)
r.mainloop()
