# BASCI CALCULATOR USING (if,elif,else)

a=int(input("Enter a number A:"))
b=int(input("Enter a number B:"))
c=input("Enter the operation:")
if(c=="+"):
    print(a+b)
elif(c=="-"):
    print(a-b)
elif(c=="*"):
    print(a*b)
elif(c=="/"):
    print(a/b)
elif(c=="%"):
    print(a%b)
elif(c=="//"):
    print(a//b)
elif(c=="**"):
    print(a**b)


